package com.example.sanmi_telesoft.beans;

public class Serenazgo {
    private int idSerenazgos;
    private String nombreSereno;
    private String apellidoSereno;
    private String dniSereno;
    private String turnoSereno;
    private String direccionSereno;
    private String telefonoSereno;
    private String isEnable;
    private TipoSereno tipoSereno;

    public int getIdSerenazgos() {
        return idSerenazgos;
    }

    public void setIdSerenazgos(int idSerenazgos) {
        this.idSerenazgos = idSerenazgos;
    }

    public String getNombreSereno() {
        return nombreSereno;
    }

    public void setNombreSereno(String nombreSereno) {
        this.nombreSereno = nombreSereno;
    }

    public String getApellidoSereno() {
        return apellidoSereno;
    }

    public void setApellidoSereno(String apellidoSereno) {
        this.apellidoSereno = apellidoSereno;
    }

    public String getDniSereno() {
        return dniSereno;
    }

    public void setDniSereno(String dniSereno) {
        this.dniSereno = dniSereno;
    }

    public String getTurnoSereno() {
        return turnoSereno;
    }

    public void setTurnoSereno(String turnoSereno) {
        this.turnoSereno = turnoSereno;
    }

    public String getDireccionSereno() {
        return direccionSereno;
    }

    public void setDireccionSereno(String direccionSereno) {
        this.direccionSereno = direccionSereno;
    }

    public String getTelefonoSereno() {
        return telefonoSereno;
    }

    public void setTelefonoSereno(String telefonoSereno) {
        this.telefonoSereno = telefonoSereno;
    }

    public String getIsEnable() {
        return isEnable;
    }

    public void setIsEnable(String isEnable) {
        this.isEnable = isEnable;
    }

    public TipoSereno getTipoSereno() {
        return tipoSereno;
    }

    public void setTipoSereno(TipoSereno tipoSereno) {
        this.tipoSereno = tipoSereno;
    }
}
